let currentLanguage = localStorage.getItem("vibelinkWebsiteLanguage") || "en";

function applyLanguage(language) {
  const dict = translations[language] || translations.en;
  document.documentElement.lang = language === "zh" ? "zh-CN" : "en";
  document.body.classList.toggle("zh", language === "zh");
  document.title = language === "zh"
    ? "VibeLink / 鹊桥 | 手机优先的远程开发控制台"
    : "VibeLink | Phone-first remote development controller";

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.getAttribute("data-i18n");
    if (dict[key]) node.textContent = dict[key];
  });

  const images = imageMap[language] || imageMap.en;
  document.getElementById("heroAppImage").src = images.app;
  document.getElementById("shotControls").src = images.controls;
  document.getElementById("shotQuick").src = images.quick;
  document.getElementById("shotCommands").src = images.commands;
  document.getElementById("shotShortcuts").src = images.shortcuts;

  document.getElementById("languageToggle").textContent = language === "zh" ? "English" : "中文";
  localStorage.setItem("vibelinkWebsiteLanguage", language);
  currentLanguage = language;
  applyTableLabels(language);
}

function applyTableLabels(language) {
  const labels = tableLabels[language] || tableLabels.en;
  document.querySelectorAll(".comparison-table .table-row:not(.table-head)").forEach((row) => {
    row.querySelectorAll("[role='cell']").forEach((cell, index) => {
      cell.setAttribute("data-label", labels[index] || "");
    });
  });
}

document.getElementById("languageToggle").addEventListener("click", () => {
  applyLanguage(currentLanguage === "zh" ? "en" : "zh");
});

document.getElementById("year").textContent = new Date().getFullYear();
applyLanguage(currentLanguage);
